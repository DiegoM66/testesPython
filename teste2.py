s = "Hello World"

#Primeira letra (H)
print(s[0])

#Remove 5 primeiros caracteres
print(s[5:])

#Remove 5 ultimos caracteres
print(s[:5])

#Inverte
print(s[::-1])

#Numero de caracteres de s
print(len(s))

#Multiplicar
letter = 'z'
print(letter*10)
print('\n'*2)

#CapsLock/Caixa Alta
print(s.upper())
print('\n')

#Caixa Baixa
print(s.lower())
print('\n')

#Dividir palavras
print(s.split())
print('\n')

#Dividir elemento especifico
print(s.split('W'))
